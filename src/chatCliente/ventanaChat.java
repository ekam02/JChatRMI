    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        String usuario = txtNick.getText();  
        if(usuario.equals("")){
            JOptionPane.showMessageDialog(null, "Escoja un Nickname v√°lido.");
        }
        else{
            try {
                location = chat.randomLocation();
                System.out.println("Imagen asignada: "+image);
                System.out.println("localizacion dada: "+location);
                if(!chat.starSession(usuario, image, location)){
                     JOptionPane.showMessageDialog(null, "Este Nickname ya existe.\nEscriba otro...");
                }
                else{
                    chat2 = new ventana2Chat(chat, usuario, image);  
                    chat2.setVisible(true); 
                    this.dispose();
                }
            } catch (RemoteException ex) {
            Logger.getLogger(ventanaChat.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnIniciarActionPerformed

